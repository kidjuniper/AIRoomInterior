    case bedroom = "Bedroom"
    case kitchen = "Kitchen"
    case bathroom = "Bathroom"
    case diningRoom = "Dining room"
    case office = "Office"
    case hotelRoom = "Hotel room"
    case restaurant = "Restaurant"
    case studyRoom = "Study room"
}
